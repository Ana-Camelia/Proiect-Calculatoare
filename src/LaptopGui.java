    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
   
 
          if (jGarantie.getText().isEmpty() || jPret.getText().isEmpty() || jBaterie.getText().isEmpty() || jDisplay.getText().isEmpty())
          jTextArea1.setText("Eroare...exista campuri necompletate");
      else {
        int i=Main.Lt.size();
        Main.Lt.add(new Laptop());
        Main.Lt.get(i).producator=jComboBox2.getSelectedItem().toString();
        Main.Lt.get(i).pret=Float.valueOf(jPret.getText());
        Main.Lt.get(i).garantie=Integer.valueOf(jGarantie.getText());
        Main.Lt.get(i).refurbished=jRefurbished.isSelected();
        //Main.Lt.get(i).SO=jSo.getText();
        Main.Lt.get(i).procesor=jComboBox1.getSelectedItem().toString();
        if (jRadioButton1.isSelected())
            Main.Lt.get(i).RAM=4;
        else if (jRadioButton2.isSelected())
            Main.Lt.get(i).RAM=8;
        else if (jRadioButton3.isSelected())
            Main.Lt.get(i).RAM=16;
        
       // Main.Lt.get(i).RAM=Integer.valueOf(jRam.getText());
       if (jRadioButton4.isSelected())
            Main.Lt.get(i).HDD=500;
       else if (jRadioButton5.isSelected())
            Main.Lt.get(i).HDD=1024;
       else if (jRadioButton6.isSelected())
            Main.Lt.get(i).HDD=2048;
    
       // Main.Lt.get(i).HDD=Integer.valueOf(jHdd.getText());
       if (jRadioButton7.isSelected())
            Main.Lt.get(i).SO="Windows 10";
       else if (jRadioButton8.isSelected())
            Main.Lt.get(i).SO="Linux";
       else if (jRadioButton9.isSelected())
            Main.Lt.get(i).SO="Free Dos";
    
      
        Main.Lt.get(i).baterie=Integer.valueOf(jBaterie.getText());
        Main.Lt.get(i).diagonala=Float.valueOf(jDisplay.getText());
        Main.Lt.get(i).touchscreen=jTouch.isSelected();
        Main.Lt.get(i).dedicata=jGpu.isSelected();
        Main.scriereL(i);

       
          }
    }//GEN-LAST:event_jButton4ActionPerformed

