    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        clearFields();  
        int k=0;
        float x,y;
        for (int i=0; i< Main.Lt.size(); i++)  
        {
            if(!jPret.getText().equals(""))
                x=Float.valueOf(jPret.getText());
            else
                x=0f;
            if(!jDisplay.getText().equals(""))
                y=Float.valueOf(jDisplay.getText());
            else
                y=0f;
            if ((Main.Lt.get(i).producator.equals(jComboBox2.getSelectedItem().toString()) || jComboBox2.getSelectedItem().toString().equals(""))
                && (Main.Lt.get(i).pret==x || jPret.getText().equals(""))
                && (Integer.toString(Main.Lt.get(i).garantie).equals(jGarantie.getText()) || jGarantie.getText().equals("")) 
                && (Main.Lt.get(i).refurbished==jRefurbished.isSelected())
                && (Main.Lt.get(i).procesor.equals(jComboBox1.getSelectedItem().toString()) || jComboBox1.getSelectedItem().toString().equals(""))
                && ((Main.Lt.get(i).RAM==4 && jRadioButton1.isSelected()) || (Main.Lt.get(i).RAM==8 && jRadioButton2.isSelected()) || (Main.Lt.get(i).RAM==16 && jRadioButton3.isSelected() ) || (!jRadioButton1.isSelected() && !jRadioButton2.isSelected() && !jRadioButton3.isSelected())) 
                && ((Main.Lt.get(i).HDD==500 && jRadioButton4.isSelected())|| (Main.Lt.get(i).HDD==1024 && jRadioButton5.isSelected()) || (Main.Lt.get(i).HDD==2048 && jRadioButton6.isSelected()) || (!jRadioButton4.isSelected() && !jRadioButton5.isSelected() && !jRadioButton6.isSelected()))
                && ((Main.Lt.get(i).SO.equals("Windows 10") && jRadioButton7.isSelected())|| (Main.Lt.get(i).SO.equals("Linux") && jRadioButton8.isSelected()) || (Main.Lt.get(i).SO.equals("Free Dos") && jRadioButton9.isSelected())|| (!jRadioButton7.isSelected() && !jRadioButton8.isSelected() && !jRadioButton9.isSelected()))
                && (Integer.toString(Main.Lt.get(i).baterie).equals(jBaterie.getText()) || jBaterie.getText().equals(""))
                && (Main.Lt.get(i).diagonala==y || jDisplay.getText().equals(""))
                && (Main.Lt.get(i).touchscreen==jTouch.isSelected())
                && (Main.Lt.get(i).dedicata==jGpu.isSelected())
                )
            {
           jTextArea1.append(Main.Lt.get(i).toString());
           k++;
        }}
           jTextArea1.append("\nBucati ramase in stoc: "+ k+'\n');
    }//GEN-LAST:event_jButton6ActionPerformed

